package com.altaf.catatankotlin.operator

fun main() {

    val tambah : Int = 12+ 3
    println("Hasil penjumlahan dari 12 + 3 = $tambah")

    val kurang : Int = 3929 - 311
    println("Hasil pengurangan dari 3929 - 311= $kurang ")

    val kali : Int = 12 * 3
    println("hasil perkalian dari 12 * 3 = $kali")

    val bagi : Int = 12 / 3
    println("hasil pembagian dari 12 / 3 = $bagi")

    val modus : Int = 7 % 2
    println("hasil modus dari 7 % 2 = $modus")

    val pangkat : Double = Math.pow(2.0 , 4.0)
    println("Hasil perpangkatan dari 2 pangkat 4 = $pangkat")

}
