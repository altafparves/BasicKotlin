package com.altaf.catatankotlin
fun main(){
    println("hello world")
    println("hello\tworld") //berfungsi untuk memasukkan identasi (memberikan tab)
    println("Hello\nzworld") // berfungsi untuk memindahkan kalimat setelahnya ke baris baru dibawah
    println("\'Hello world") // untuk menampilkan tanda petik satu (')
    println("\"hello world") // untuk menampilkan tanda petik dua (")
    println("\\hello world") // utnuk menampilkan tanda slash(\)
    println("\$Hello world") // untuk menampilkan simbol dolar ($)

}



